const express = require('express');
const router = express.Router();
const {
    signup,
    login,
    getMe,
    changePassword,
    forgotPassword,
    resetPassword,
    sendOtp,
    verifyOtp,
    logout
} = require('../controllers/authController');

const { verifyToken } = require('../middlewares/authMiddleware');

